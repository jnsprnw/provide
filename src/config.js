function generateValue (type) {
  let value = Math.random();
  if (type === 'year') {
    value = Math.round(value * 2000);
  } else if (type === 'length') {
    value = Math.round(value * 50);
  }

  return {
    "value": value,
    "hasWarning": Math.random() < 0.5,
    "type": "celsius"
  }
}

